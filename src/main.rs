struct MyGame {
    world: World
}

impl MyGame {
    pub fn new(_ctx: &mut Context, world: World) -> MyGame {
        // Load/create resources such as images here.
        MyGame {
            world
        }
    }
}

impl event::EventHandler for MyGame {
    fn update(&mut self, _context: &mut Context) -> GameResult {
        // TODO: update game logic here
        Ok(())
    }

    fn draw(&mut self, context: &mut Context) -> GameResult {
        // Render game entities
        {
            let mut rs = RenderingSystem { context };
            rs.run_now(&self.world);
        }
        Ok(())
    }
}

pub fn main() -> GameResult {
    let mut world = World::new();
    register_components(&mut world);

    let cb = ggez::ContextBuilder::new("rust_sokoban", "sokoban")
        .window_setup(conf::WindowSetup::default().title("Rust Sokoban!"))
        .window_mode(conf::WindowMode::default().dimensions(1024.0, 768.0))
        .add_resource_path(path::PathBuf::from("./resources"));

    let (context, event_loop) = &mut cb.build()?;
    let mut game = MyGame::new(context, world);
    event::run(context, event_loop,  &mut game)
}
